-- build missing urls and names or data automatically as well as grabbing the contents of data xml files

for i,v in ipairs(urlcache) do

	if not v.file then
	
		v.file= "art/data/"..v.base
		
-- use the www location, instead of local copy?		
		v.file= "../../www/wetgenes/subs/data/game/s/ville/"..v.base
		
	end

	if not v.url then
	
		v.url= "http://data.wetgenes.com/game/s/ville/"..v.base
		
	end
	
	if not v.name then
	
		v.name= "data_".. string.gsub( v.base , "[^A-Za-z0-9]" , "_")
		
	end
	
	if string.sub(v.base,-3)=="xml" then -- an xml file, cache the contents
	
	local fp
	
		fp=io.open(v.file,"r")
		v.xml=fp:read("*a")
		fp:close()
	
	else
		v.img=v.file	
	end

end
